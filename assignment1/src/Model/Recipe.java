package Model;

/**
 *
 * @author caofei
 */
public class Recipe {
    
    String recT;
    float noS;
    String igF;
    double dL;
    int noI;
    String coF;
    String deS;
    
    public Recipe(){
    }

   

    public String getRecT() {
        return recT;
    }

    public void setRecT(String recT) {
        this.recT = recT;
    }

    public float getNoS() {
        return noS;
    }

    public void setNoS(float noS) {
        this.noS = noS;
    }

    public String getIgF() {
        return igF;
    }

    public void setIgF(String igF) {
        this.igF = igF;
    }

    public double getdL() {
        return dL;
    }

    public void setdL(double dL) {
        this.dL = dL;
    }

    public int getNoI() {
        return noI;
    }

    public void setNoI(int noI) {
        this.noI = noI;
    }

    public String getCoF() {
        return coF;
    }

    public void setCoF(String coF) {
        this.coF = coF;
    }

    public String getDeS() {
        return deS;
    }

    public void setDeS(String deS) {
        this.deS = deS;
    }
    
}
