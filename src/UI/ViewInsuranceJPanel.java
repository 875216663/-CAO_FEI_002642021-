    private void updateObservationbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateObservationbtnActionPerformed
        // TODO add your handling code here:
        if(!planIdfield.getText().isEmpty()) {
            PlanDetail o = this.business.getInsPlan().findObservation(Integer.valueOf(planIdfield.getText()));
            o.setPlanName(String.valueOf(planNamefield.getText()));
            o.setCostperMonth(Integer.valueOf(planCostfield.getText()));
            
            JOptionPane.showMessageDialog(null, "Updated successfully");
        } else {
            JOptionPane.showMessageDialog(null, "You have not made any selection");
        }
        
    }//GEN-LAST:event_updateObservationbtnActionPerformed

    private void viewObservationbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewObservationbtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = observationTable.getSelectedRow();
        if(selectedRow >= 0) {
            this.selectedPlan = (PlanDetail) observationTable.getValueAt(selectedRow, 0);
            

            planIdfield.setText(String.valueOf(selectedPlan.getPlanID()));
            planNamefield.setText(String.valueOf(selectedPlan.getPlanName()));
            planCostfield.setText(String.valueOf(selectedPlan.getCostperMonth()));
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row!");
        }
    }//GEN-LAST:event_viewObservationbtnActionPerformed

