     public void displayplanDetail() {
        InsurancePlan plan = this.business.getInsPlan();
        
        if(plan.getInsurancePlan().size() > 0) {//如果这个数列不是0 的话
            viewtableModel.setRowCount(0);
            for(PlanDetail o: plan.getInsurancePlan()) {
                Object row[] = new Object[3];
                row[0] = o;
                row[1] = o.getPlanName();
                row[2] = o.getCostperMonth();
                
                viewtableModel.addRow(row);
            }
        } else {
            System.out.println("Empty list");
        }
    }

